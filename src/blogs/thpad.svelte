  <h1>Falling in love with an 8 year laptop</h1>
  <h3>February 13th, 2024.</h3>
  <p>
    I'll start this blog post by giving a bit of background; I daily drive a
    $2000 Windows 11 gaming PC that I've been gradually upgrading since around
    September of 2021. To say it has served me well would be the understatement
    of the century. However, my whole life I've never truly used a laptop nor
    any operating system other than Windows outside of my adolescence, borrowing
    my mom's work computer to play stupid flash games, which later down the road
    got me interested in web development, but I digress.
  </p>

  <p>
    Several years of using Windows and hearing about the awesome utility for
    developers on Linux left my eyes wandering, but my problem was not wanting
    to uninstall Windows on my main PC, as I wouldn't be able to play a lot of
    my favorite games. Nor did I want to dual boot Linux for other reasons. This
    left me with two options. One: forget about using Linux and continue being
    content with Windows, or two: buy another computer. The answer was obvious
    to me. I scoured the internet in search of a portable, powerful (enough)
    laptop that wouldn't break the bank, and to say those requirements were not
    only met, but surpassed when I stumbled upon the earlier models of the
    Lenovo Thinkpad T series of laptops would be yet another understatement. I
    fell in love with the design, form-factor and keyboard of the T420, and for
    only 120 USD, it was mine.
  </p>

  <h3>Things I Like</h3>
  <ul>
    <li>
      <strong>Form Factor:</strong> The thicker yet still portable design of the
      T420 fits perfectly into my lifestyle, allowing me to use it with ease wherever
      I go.
    </li>
    <li>
      <strong>Upgradability:</strong> One of the standout features of the T420 is
      its upgradability, allowing you to customize and improve its performance as
      needed, increasing its lifespan by literal YEARS.
    </li>
    <li>
      <strong>Battery Life:</strong> Despite its age, the T420 boasts impressive
      battery life, especially with an aftermarket, higher capacity battery, keeping
      me productive throughout the day without constant need for charging.
    </li>
    <li>
      <strong>Linux Compatibility:</strong> Transitioning to Linux was seamless with
      the T420, thanks to its excellent compatibility and support for various distributions.
    </li>
    <li>
      <strong>Being Just Powerful Enough:</strong> While not the latest powerhouse,
      the T420 offers just the right level of performance for my daily tasks and
      development work.
    </li>
    <li>
      <strong>Keyboard:</strong> Regardless of it not being comparable to higher-end
      or custom mechanical keyboards, the keyboard on the T420 is a joy to type on,
      providing tactile feedback and comfort for long typing sessions.
    </li>
    <li>
      <strong>Charm:</strong> Little details like the keyboard light add to the overall
      charm and usability of the T420, enhancing the user experience.
    </li>
    <li>
      <strong>The IO Port Selection:</strong> The T420 offers a variety of ports,
      including two full sized USB ports, full sized Ethernet and display port, ensuring
      connectivity options for all my peripherals and accessories.
    </li>
    <li>
      <strong>Developer Experience:</strong> As a developer, the T420 provides a
      smooth and efficient environment for coding and testing, enhancing my productivity.
    </li>
    <li>
      <strong>Manjaro:</strong> While not without its quirks, Manjaro has been a
      reliable and user-friendly Linux distribution for my needs, further enhancing
      the T420's appeal.
    </li>
  </ul>

  <h3>Things I Don't Like</h3>
  <ul>
    <li>
      <strong>The Fn Button Being Where the Ctrl Should:</strong> The placement of
      the Fn button can be inconvenient at times, especially for users accustomed
      to the traditional layout.
    </li>
    <li>
      <strong>The Latch to Open the Lid:</strong> While a minor inconvenience, the
      latch mechanism for opening the lid feels outdated and unnecessary.
    </li>
    <li>
      <strong>The Lack of USB3:</strong> In today's age of fast data transfer, the
      absence of USB3 ports on the T420 can be limiting for certain tasks.
    </li>
    <li>
      <strong>The CD Drive:</strong> The inclusion of a CD drive feels outdated and
      takes up valuable space that could be used for more modern features or components.
    </li>
    <li>
      <strong>Manjaro:</strong> While Manjaro offers great features, its stability
      and compatibility issues can be frustrating at times, detracting from the overall
      experience.
    </li>
  </ul>

  <p>
    In conclusion, my journey with the Lenovo Thinkpad T420 has been a rewarding
    one, as it has allowed me to embrace Linux and expand my horizons in terms
    of operating systems and development environments. While not without its
    flaws, the T420's affordability, versatility, and charm make it a worthy
    companion for anyone looking to explore the world of Linux on a budget.
  </p>
